package Beans;

public class RoutesHaltesBean {
    /**
     * ID маршрута
     */
    private int routeID;
    /**
     * ID остановки
     */
    private int haltID;

    public int getRouteID() {
        return routeID;
    }

    public void setRouteID(int routeID) {
        this.routeID = routeID;
    }

    public int getHaltID() {
        return haltID;
    }

    public void setHaltID(int haltID) {
        this.haltID = haltID;
    }
}
