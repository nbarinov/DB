package Beans;

public class HaltesBean {
    /**
     * ID остановки
     */
    private int haltID;
    /**
     * Название остановки
     */
    private String haltName;

    public int getHaltID() {
        return haltID;
    }

    public void setHaltID(int haltID) {
        this.haltID = haltID;
    }

    public String getHaltName() {
        return haltName;
    }

    public void setHaltName(String haltName) {
        this.haltName = haltName;
    }
}
